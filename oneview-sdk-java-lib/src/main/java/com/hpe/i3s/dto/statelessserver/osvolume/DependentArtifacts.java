package com.hpe.i3s.dto.statelessserver.osvolume;

import java.io.Serializable;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

public class DependentArtifacts implements Serializable {

    private static final long serialVersionUID = 1L;

    private String name;
    private String serverIqn;
    private String uri;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the serverIqn
     */
    public String getServerIqn() {
        return serverIqn;
    }

    /**
     * @param serverIqn the serverIqn to set
     */
    public void setServerIqn(String serverIqn) {

        this.serverIqn = serverIqn;
    }
    /**
     * @return the uri
     */
    public String getUri() {
        return uri;
    }

    /**
     * @param uri the uri to set
     */
    public void setUri(String uri) {
        this.uri = uri;
    }

    @Override
    public boolean equals(Object obj) {
        return EqualsBuilder.reflectionEquals(this, obj);
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

    @Override
    public String toString() {
        return ToStringBuilder.reflectionToString(this);
    }
}
